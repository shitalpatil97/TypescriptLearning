class Arithmetic{
    add(arg1:number,arg2:number):number{
        return arg1+arg2;
    }
    sub(arg1:number,arg2:number):number{
        return arg1-arg2;
    }
    mul(arg1:number,arg2:number):number{
        return arg1*arg2;
    }
    div(arg1:number,arg2:number):number{
        return arg1/arg2;
    }
}
export {Arithmetic};